#include "Init.h"

void System_Init()
{
	P0 = 0xff; //Led 全关
	P2 = P2 & 0x1f | 0x80; // 0x1f = 00011111 0x80 = 10000000
	P2 &= 0x1f;
	
	P0 = 0x00; //蜂鸣器 关
	P2 = P2 & 0x1f | 0xa0;// 0xa0 = 10100000 74h138的Y5给0 经过或非门 Y5和WR都为0时 Y5C 输出为 1 ，则锁存器使能，左边数据可以通过右边数据
	P2 &= 0x1f; //P2  高三位为0 则 74h138的Y5给1 经过或非门 Y5C 输出为 0 ，则锁存器锁住数据
}
 
