#include <Seg.h>

unsigned char Dula[] = {0xc0,0xf9,0xa4,0xb0,0x99,0x92,0x82,0xf8,0x80,0x90,0xff,0xc6,0x86,0x89};//11C 12E 13H
unsigned char Wela[] = {0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80};


void Seg_Disp(unsigned char Seg_Dula,Seg_Wela,Seg_Point)
{
	unsigned char temp;
	
	P0 = 0xff;
	temp = (P2 & 0x1f) | 0xe0;
	P2 = temp;
	temp = P2 & 0x1f;
	P2 = temp;
	
	P0 = Wela[Seg_Wela];
	temp = (P2 & 0x1f) | 0xc0;
	P2 = temp;
	temp = P2 & 0x1f;
	P2 = temp;

	P0 = Dula[Seg_Dula];
	if(Seg_Point)
		P0 &= 0x7f;
	temp = (P2 & 0x1f) | 0xe0;
	P2 = temp;
	temp = P2 & 0x1f;
	P2 = temp;
	

}	
